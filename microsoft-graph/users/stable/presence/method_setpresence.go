type SetPresenceOperationResponse struct {
	HttpResponse *http.Response
	OData        *odata.OData
}

// SetPresence - Invoke action setPresence. Set the state of a user's presence session as an application.
func (c PresenceClient) SetPresence(ctx context.Context, id stable.UserId, input SetPresenceRequest) (result SetPresenceOperationResponse, err error) {
	opts := client.RequestOptions{
		ContentType: "application/json; charset=utf-8",
		ExpectedStatusCodes: []int{
			http.StatusNoContent,
		},
		HttpMethod: http.MethodPost,
		Path:       fmt.Sprintf("%s/presence/setPresence", id.ID()),
	}

	req, err := c.Client.NewRequest(ctx, opts)
	if err != nil {
		return
	}

	if err = req.Marshal(input); err != nil {
		return
	}

	var resp *client.Response
	resp, err = req.Execute(ctx)
	if resp != nil {
		result.OData = resp.OData
		result.HttpResponse = resp.Response
	}
	if err != nil {
		return
	}

	return
}
