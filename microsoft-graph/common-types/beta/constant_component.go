type Component string

const (
	Component_Label Component = "Label"
)

func PossibleValuesForComponent() []string {
	return []string{
		string(Component_Label),
	}
}

func (s *Component) UnmarshalJSON(bytes []byte) error {
	var decoded string
	if err := json.Unmarshal(bytes, &decoded); err != nil {
		return fmt.Errorf("unmarshaling: %+v", err)
	}
	out, err := parseComponent(decoded)
	if err != nil {
		return fmt.Errorf("parsing %q: %+v", decoded, err)
	}
	*s = *out
	return nil
}

func parseComponent(input string) (*Component, error) {
	vals := map[string]Component{
		"label": Component_Label,
	}
	if v, ok := vals[strings.ToLower(input)]; ok {
		return &v, nil
	}

	// otherwise presume it's an undefined value and best-effort it
	out := Component(input)
	return &out, nil
}
