type SetDriveItemVersionContentOperationResponse struct {
	HttpResponse *http.Response
	OData        *odata.OData
}

// SetDriveItemVersionContent - Update content for the navigation property versions in me. The content stream for this
// version of the item.
func (c DriveItemVersionContentClient) SetDriveItemVersionContent(ctx context.Context, id stable.MeDriveIdItemIdVersionId, input []byte) (result SetDriveItemVersionContentOperationResponse, err error) {
	opts := client.RequestOptions{
		ContentType: "application/json; charset=utf-8",
		ExpectedStatusCodes: []int{
			http.StatusNoContent,
		},
		HttpMethod: http.MethodPut,
		Path:       fmt.Sprintf("%s/content", id.ID()),
	}

	req, err := c.Client.NewRequest(ctx, opts)
	if err != nil {
		return
	}

	if err = req.Marshal(input); err != nil {
		return
	}

	var resp *client.Response
	resp, err = req.Execute(ctx)
	if resp != nil {
		result.OData = resp.OData
		result.HttpResponse = resp.Response
	}
	if err != nil {
		return
	}

	return
}
