type ChangePasswordOperationResponse struct {
	HttpResponse *http.Response
	OData        *odata.OData
}

// ChangePassword - Invoke action changePassword. Enable the user to update their password. Any user can update their
// password without belonging to any administrator role.
func (c MeClient) ChangePassword(ctx context.Context, input ChangePasswordRequest) (result ChangePasswordOperationResponse, err error) {
	opts := client.RequestOptions{
		ContentType: "application/json; charset=utf-8",
		ExpectedStatusCodes: []int{
			http.StatusNoContent,
		},
		HttpMethod: http.MethodPost,
		Path:       "/me/changePassword",
	}

	req, err := c.Client.NewRequest(ctx, opts)
	if err != nil {
		return
	}

	if err = req.Marshal(input); err != nil {
		return
	}

	var resp *client.Response
	resp, err = req.Execute(ctx)
	if resp != nil {
		result.OData = resp.OData
		result.HttpResponse = resp.Response
	}
	if err != nil {
		return
	}

	return
}
