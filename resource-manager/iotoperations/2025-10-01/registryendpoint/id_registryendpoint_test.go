func TestNewRegistryEndpointID(t *testing.T) {
	id := NewRegistryEndpointID("12345678-1234-9876-4563-123456789012", "example-resource-group", "instanceName", "registryEndpointName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.InstanceName != "instanceName" {
		t.Fatalf("Expected %q but got %q for Segment 'InstanceName'", id.InstanceName, "instanceName")
	}

	if id.RegistryEndpointName != "registryEndpointName" {
		t.Fatalf("Expected %q but got %q for Segment 'RegistryEndpointName'", id.RegistryEndpointName, "registryEndpointName")
	}
}

func TestFormatRegistryEndpointID(t *testing.T) {
	actual := NewRegistryEndpointID("12345678-1234-9876-4563-123456789012", "example-resource-group", "instanceName", "registryEndpointName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.IoTOperations/instances/instanceName/registryEndpoints/registryEndpointName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

