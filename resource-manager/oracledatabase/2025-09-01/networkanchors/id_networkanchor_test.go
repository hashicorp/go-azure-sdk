var _ resourceids.ResourceId = &NetworkAnchorId{}

func TestNewNetworkAnchorID(t *testing.T) {
	id := NewNetworkAnchorID("12345678-1234-9876-4563-123456789012", "example-resource-group", "networkAnchorName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.NetworkAnchorName != "networkAnchorName" {
		t.Fatalf("Expected %q but got %q for Segment 'NetworkAnchorName'", id.NetworkAnchorName, "networkAnchorName")
	}
}

func TestFormatNetworkAnchorID(t *testing.T) {
	actual := NewNetworkAnchorID("12345678-1234-9876-4563-123456789012", "example-resource-group", "networkAnchorName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Oracle.Database/networkAnchors/networkAnchorName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

