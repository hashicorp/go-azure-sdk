func TestNewDbSystemID(t *testing.T) {
	id := NewDbSystemID("12345678-1234-9876-4563-123456789012", "example-resource-group", "dbSystemName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.DbSystemName != "dbSystemName" {
		t.Fatalf("Expected %q but got %q for Segment 'DbSystemName'", id.DbSystemName, "dbSystemName")
	}
}

func TestFormatDbSystemID(t *testing.T) {
	actual := NewDbSystemID("12345678-1234-9876-4563-123456789012", "example-resource-group", "dbSystemName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Oracle.Database/dbSystems/dbSystemName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

