func TestNewSchemaID(t *testing.T) {
	id := NewSchemaID("12345678-1234-9876-4563-123456789012", "example-resource-group", "integrationAccountValue", "schemaValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.IntegrationAccountName != "integrationAccountValue" {
		t.Fatalf("Expected %q but got %q for Segment 'IntegrationAccountName'", id.IntegrationAccountName, "integrationAccountValue")
	}

	if id.SchemaName != "schemaValue" {
		t.Fatalf("Expected %q but got %q for Segment 'SchemaName'", id.SchemaName, "schemaValue")
	}
}

func TestFormatSchemaID(t *testing.T) {
	actual := NewSchemaID("12345678-1234-9876-4563-123456789012", "example-resource-group", "integrationAccountValue", "schemaValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Logic/integrationAccounts/integrationAccountValue/schemas/schemaValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

