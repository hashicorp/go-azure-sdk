func TestNewTuningOptionID(t *testing.T) {
	id := NewTuningOptionID("12345678-1234-9876-4563-123456789012", "example-resource-group", "flexibleServerName", "index")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.FlexibleServerName != "flexibleServerName" {
		t.Fatalf("Expected %q but got %q for Segment 'FlexibleServerName'", id.FlexibleServerName, "flexibleServerName")
	}

	if id.TuningOption != "index" {
		t.Fatalf("Expected %q but got %q for Segment 'TuningOption'", id.TuningOption, "index")
	}
}

