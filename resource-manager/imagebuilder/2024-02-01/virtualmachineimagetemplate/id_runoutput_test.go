func TestNewRunOutputID(t *testing.T) {
	id := NewRunOutputID("12345678-1234-9876-4563-123456789012", "example-resource-group", "imageTemplateName", "runOutputName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ImageTemplateName != "imageTemplateName" {
		t.Fatalf("Expected %q but got %q for Segment 'ImageTemplateName'", id.ImageTemplateName, "imageTemplateName")
	}

	if id.RunOutputName != "runOutputName" {
		t.Fatalf("Expected %q but got %q for Segment 'RunOutputName'", id.RunOutputName, "runOutputName")
	}
}

