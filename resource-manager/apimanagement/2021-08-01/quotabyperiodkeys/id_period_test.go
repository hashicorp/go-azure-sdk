func TestNewPeriodID(t *testing.T) {
	id := NewPeriodID("12345678-1234-9876-4563-123456789012", "example-resource-group", "serviceValue", "quotaCounterKeyValue", "quotaPeriodKeyValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ServiceName != "serviceValue" {
		t.Fatalf("Expected %q but got %q for Segment 'ServiceName'", id.ServiceName, "serviceValue")
	}

	if id.QuotaCounterKey != "quotaCounterKeyValue" {
		t.Fatalf("Expected %q but got %q for Segment 'QuotaCounterKey'", id.QuotaCounterKey, "quotaCounterKeyValue")
	}

	if id.QuotaPeriodKey != "quotaPeriodKeyValue" {
		t.Fatalf("Expected %q but got %q for Segment 'QuotaPeriodKey'", id.QuotaPeriodKey, "quotaPeriodKeyValue")
	}
}

func TestFormatPeriodID(t *testing.T) {
	actual := NewPeriodID("12345678-1234-9876-4563-123456789012", "example-resource-group", "serviceValue", "quotaCounterKeyValue", "quotaPeriodKeyValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.ApiManagement/service/serviceValue/quotas/quotaCounterKeyValue/periods/quotaPeriodKeyValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

