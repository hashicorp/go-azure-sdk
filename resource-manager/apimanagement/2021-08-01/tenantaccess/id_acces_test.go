var _ resourceids.ResourceId = AccesId{}

func TestNewAccesID(t *testing.T) {
	id := NewAccesID("12345678-1234-9876-4563-123456789012", "example-resource-group", "serviceValue", "access")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ServiceName != "serviceValue" {
		t.Fatalf("Expected %q but got %q for Segment 'ServiceName'", id.ServiceName, "serviceValue")
	}

	if id.AccessName != "access" {
		t.Fatalf("Expected %q but got %q for Segment 'AccessName'", id.AccessName, "access")
	}
}

func TestFormatAccesID(t *testing.T) {
	actual := NewAccesID("12345678-1234-9876-4563-123456789012", "example-resource-group", "serviceValue", "access").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.ApiManagement/service/serviceValue/tenant/access"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

