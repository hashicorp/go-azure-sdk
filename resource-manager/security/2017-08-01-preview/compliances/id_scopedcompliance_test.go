var _ resourceids.ResourceId = ScopedComplianceId{}

func TestNewScopedComplianceID(t *testing.T) {
	id := NewScopedComplianceID("/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/some-resource-group", "complianceValue")

	if id.Scope != "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/some-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'Scope'", id.Scope, "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/some-resource-group")
	}

	if id.ComplianceName != "complianceValue" {
		t.Fatalf("Expected %q but got %q for Segment 'ComplianceName'", id.ComplianceName, "complianceValue")
	}
}

