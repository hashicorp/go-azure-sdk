var _ resourceids.ResourceId = &ProviderPricingId{}

func TestNewProviderPricingID(t *testing.T) {
	id := NewProviderPricingID("12345678-1234-9876-4563-123456789012", "example-resource-group", "pricingValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.PricingName != "pricingValue" {
		t.Fatalf("Expected %q but got %q for Segment 'PricingName'", id.PricingName, "pricingValue")
	}
}

func TestFormatProviderPricingID(t *testing.T) {
	actual := NewProviderPricingID("12345678-1234-9876-4563-123456789012", "example-resource-group", "pricingValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Security/pricings/pricingValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

