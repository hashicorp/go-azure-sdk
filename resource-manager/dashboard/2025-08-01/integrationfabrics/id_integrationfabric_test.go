func TestNewIntegrationFabricID(t *testing.T) {
	id := NewIntegrationFabricID("12345678-1234-9876-4563-123456789012", "example-resource-group", "grafanaName", "integrationFabricName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.GrafanaName != "grafanaName" {
		t.Fatalf("Expected %q but got %q for Segment 'GrafanaName'", id.GrafanaName, "grafanaName")
	}

	if id.IntegrationFabricName != "integrationFabricName" {
		t.Fatalf("Expected %q but got %q for Segment 'IntegrationFabricName'", id.IntegrationFabricName, "integrationFabricName")
	}
}

