var _ resourceids.ResourceId = &PoolId{}

func TestNewPoolID(t *testing.T) {
	id := NewPoolID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountName", "poolName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.BatchAccountName != "accountName" {
		t.Fatalf("Expected %q but got %q for Segment 'BatchAccountName'", id.BatchAccountName, "accountName")
	}

	if id.PoolName != "poolName" {
		t.Fatalf("Expected %q but got %q for Segment 'PoolName'", id.PoolName, "poolName")
	}
}

func TestFormatPoolID(t *testing.T) {
	actual := NewPoolID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountName", "poolName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Batch/batchAccounts/accountName/pools/poolName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

