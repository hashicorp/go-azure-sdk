var _ resourceids.ResourceId = &RegionId{}

func TestNewRegionID(t *testing.T) {
	id := NewRegionID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountName", "region")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.DatabaseAccountName != "accountName" {
		t.Fatalf("Expected %q but got %q for Segment 'DatabaseAccountName'", id.DatabaseAccountName, "accountName")
	}

	if id.RegionName != "region" {
		t.Fatalf("Expected %q but got %q for Segment 'RegionName'", id.RegionName, "region")
	}
}

func TestFormatRegionID(t *testing.T) {
	actual := NewRegionID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountName", "region").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.DocumentDB/databaseAccounts/accountName/region/region"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

