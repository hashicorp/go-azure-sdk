type Operation string

const (
	OperationActive         Operation = "Active"
	OperationAddBegin       Operation = "AddBegin"
	OperationAddComplete    Operation = "AddComplete"
	OperationDeleteBegin    Operation = "DeleteBegin"
	OperationDeleteComplete Operation = "DeleteComplete"
)

func PossibleValuesForOperation() []string {
	return []string{
		string(OperationActive),
		string(OperationAddBegin),
		string(OperationAddComplete),
		string(OperationDeleteBegin),
		string(OperationDeleteComplete),
	}
}

func (s *Operation) UnmarshalJSON(bytes []byte) error {
	var decoded string
	if err := json.Unmarshal(bytes, &decoded); err != nil {
		return fmt.Errorf("unmarshaling: %+v", err)
	}
	out, err := parseOperation(decoded)
	if err != nil {
		return fmt.Errorf("parsing %q: %+v", decoded, err)
	}
	*s = *out
	return nil
}

func parseOperation(input string) (*Operation, error) {
	vals := map[string]Operation{
		"active":         OperationActive,
		"addbegin":       OperationAddBegin,
		"addcomplete":    OperationAddComplete,
		"deletebegin":    OperationDeleteBegin,
		"deletecomplete": OperationDeleteComplete,
	}
	if v, ok := vals[strings.ToLower(input)]; ok {
		return &v, nil
	}

	// otherwise presume it's an undefined value and best-effort it
	out := Operation(input)
	return &out, nil
}

