var _ resourceids.ResourceId = &CloudHsmClusterId{}

func TestNewCloudHsmClusterID(t *testing.T) {
	id := NewCloudHsmClusterID("12345678-1234-9876-4563-123456789012", "example-resource-group", "cloudHsmClusterName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.CloudHsmClusterName != "cloudHsmClusterName" {
		t.Fatalf("Expected %q but got %q for Segment 'CloudHsmClusterName'", id.CloudHsmClusterName, "cloudHsmClusterName")
	}
}

func TestFormatCloudHsmClusterID(t *testing.T) {
	actual := NewCloudHsmClusterID("12345678-1234-9876-4563-123456789012", "example-resource-group", "cloudHsmClusterName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.HardwareSecurityModules/cloudHsmClusters/cloudHsmClusterName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

