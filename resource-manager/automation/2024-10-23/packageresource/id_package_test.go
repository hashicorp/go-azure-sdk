func TestNewPackageID(t *testing.T) {
	id := NewPackageID("12345678-1234-9876-4563-123456789012", "example-resource-group", "automationAccountName", "runtimeEnvironmentName", "packageName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.AutomationAccountName != "automationAccountName" {
		t.Fatalf("Expected %q but got %q for Segment 'AutomationAccountName'", id.AutomationAccountName, "automationAccountName")
	}

	if id.RuntimeEnvironmentName != "runtimeEnvironmentName" {
		t.Fatalf("Expected %q but got %q for Segment 'RuntimeEnvironmentName'", id.RuntimeEnvironmentName, "runtimeEnvironmentName")
	}

	if id.PackageName != "packageName" {
		t.Fatalf("Expected %q but got %q for Segment 'PackageName'", id.PackageName, "packageName")
	}
}

func TestFormatPackageID(t *testing.T) {
	actual := NewPackageID("12345678-1234-9876-4563-123456789012", "example-resource-group", "automationAccountName", "runtimeEnvironmentName", "packageName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Automation/automationAccounts/automationAccountName/runtimeEnvironments/runtimeEnvironmentName/packages/packageName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

