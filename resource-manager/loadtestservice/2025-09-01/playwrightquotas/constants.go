type FreeTrialState string

const (
	FreeTrialStateActive        FreeTrialState = "Active"
	FreeTrialStateExpired       FreeTrialState = "Expired"
	FreeTrialStateNotApplicable FreeTrialState = "NotApplicable"
)

func PossibleValuesForFreeTrialState() []string {
	return []string{
		string(FreeTrialStateActive),
		string(FreeTrialStateExpired),
		string(FreeTrialStateNotApplicable),
	}
}

func (s *FreeTrialState) UnmarshalJSON(bytes []byte) error {
	var decoded string
	if err := json.Unmarshal(bytes, &decoded); err != nil {
		return fmt.Errorf("unmarshaling: %+v", err)
	}
	out, err := parseFreeTrialState(decoded)
	if err != nil {
		return fmt.Errorf("parsing %q: %+v", decoded, err)
	}
	*s = *out
	return nil
}

func parseFreeTrialState(input string) (*FreeTrialState, error) {
	vals := map[string]FreeTrialState{
		"active":        FreeTrialStateActive,
		"expired":       FreeTrialStateExpired,
		"notapplicable": FreeTrialStateNotApplicable,
	}
	if v, ok := vals[strings.ToLower(input)]; ok {
		return &v, nil
	}

	// otherwise presume it's an undefined value and best-effort it
	out := FreeTrialState(input)
	return &out, nil
}

