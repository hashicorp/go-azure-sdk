var _ resourceids.ResourceId = &PlaywrightWorkspaceId{}

func TestNewPlaywrightWorkspaceID(t *testing.T) {
	id := NewPlaywrightWorkspaceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "playwrightWorkspaceName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.PlaywrightWorkspaceName != "playwrightWorkspaceName" {
		t.Fatalf("Expected %q but got %q for Segment 'PlaywrightWorkspaceName'", id.PlaywrightWorkspaceName, "playwrightWorkspaceName")
	}
}

func TestFormatPlaywrightWorkspaceID(t *testing.T) {
	actual := NewPlaywrightWorkspaceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "playwrightWorkspaceName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.LoadTestService/playwrightWorkspaces/playwrightWorkspaceName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

