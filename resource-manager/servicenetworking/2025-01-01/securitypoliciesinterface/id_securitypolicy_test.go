func TestNewSecurityPolicyID(t *testing.T) {
	id := NewSecurityPolicyID("12345678-1234-9876-4563-123456789012", "example-resource-group", "trafficControllerName", "securityPolicyName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.TrafficControllerName != "trafficControllerName" {
		t.Fatalf("Expected %q but got %q for Segment 'TrafficControllerName'", id.TrafficControllerName, "trafficControllerName")
	}

	if id.SecurityPolicyName != "securityPolicyName" {
		t.Fatalf("Expected %q but got %q for Segment 'SecurityPolicyName'", id.SecurityPolicyName, "securityPolicyName")
	}
}

