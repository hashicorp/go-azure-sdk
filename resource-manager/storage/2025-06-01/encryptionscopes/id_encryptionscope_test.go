func TestNewEncryptionScopeID(t *testing.T) {
	id := NewEncryptionScopeID("12345678-1234-9876-4563-123456789012", "example-resource-group", "storageAccountName", "encryptionScopeName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.StorageAccountName != "storageAccountName" {
		t.Fatalf("Expected %q but got %q for Segment 'StorageAccountName'", id.StorageAccountName, "storageAccountName")
	}

	if id.EncryptionScopeName != "encryptionScopeName" {
		t.Fatalf("Expected %q but got %q for Segment 'EncryptionScopeName'", id.EncryptionScopeName, "encryptionScopeName")
	}
}

