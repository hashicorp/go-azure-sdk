var _ resourceids.ResourceId = &TableId{}

func TestNewTableID(t *testing.T) {
	id := NewTableID("12345678-1234-9876-4563-123456789012", "example-resource-group", "storageAccountName", "tableName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.StorageAccountName != "storageAccountName" {
		t.Fatalf("Expected %q but got %q for Segment 'StorageAccountName'", id.StorageAccountName, "storageAccountName")
	}

	if id.TableName != "tableName" {
		t.Fatalf("Expected %q but got %q for Segment 'TableName'", id.TableName, "tableName")
	}
}

func TestFormatTableID(t *testing.T) {
	actual := NewTableID("12345678-1234-9876-4563-123456789012", "example-resource-group", "storageAccountName", "tableName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Storage/storageAccounts/storageAccountName/tableServices/default/tables/tableName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

