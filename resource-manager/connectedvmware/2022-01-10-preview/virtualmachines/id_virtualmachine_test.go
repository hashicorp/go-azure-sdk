var _ resourceids.ResourceId = VirtualMachineId{}

func TestNewVirtualMachineID(t *testing.T) {
	id := NewVirtualMachineID("12345678-1234-9876-4563-123456789012", "example-resource-group", "nameValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.Name != "nameValue" {
		t.Fatalf("Expected %q but got %q for Segment 'Name'", id.Name, "nameValue")
	}
}

func TestFormatVirtualMachineID(t *testing.T) {
	actual := NewVirtualMachineID("12345678-1234-9876-4563-123456789012", "example-resource-group", "nameValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.ConnectedVMwarevSphere/virtualMachines/nameValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

