func TestNewPersonalScheduleID(t *testing.T) {
	id := NewPersonalScheduleID("12345678-1234-9876-4563-123456789012", "example-resource-group", "scalingPlanName", "personalScheduleName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ScalingPlanName != "scalingPlanName" {
		t.Fatalf("Expected %q but got %q for Segment 'ScalingPlanName'", id.ScalingPlanName, "scalingPlanName")
	}

	if id.PersonalScheduleName != "personalScheduleName" {
		t.Fatalf("Expected %q but got %q for Segment 'PersonalScheduleName'", id.PersonalScheduleName, "personalScheduleName")
	}
}

