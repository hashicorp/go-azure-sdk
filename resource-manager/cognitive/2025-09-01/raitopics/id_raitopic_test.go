func TestNewRaitopicID(t *testing.T) {
	id := NewRaitopicID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountName", "raitopicName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.AccountName != "accountName" {
		t.Fatalf("Expected %q but got %q for Segment 'AccountName'", id.AccountName, "accountName")
	}

	if id.RaitopicName != "raitopicName" {
		t.Fatalf("Expected %q but got %q for Segment 'RaitopicName'", id.RaitopicName, "raitopicName")
	}
}

func TestFormatRaitopicID(t *testing.T) {
	actual := NewRaitopicID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountName", "raitopicName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.CognitiveServices/accounts/accountName/raitopics/raitopicName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

