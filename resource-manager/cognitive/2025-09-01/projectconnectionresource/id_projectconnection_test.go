func TestNewProjectConnectionID(t *testing.T) {
	id := NewProjectConnectionID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountName", "projectName", "connectionName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.AccountName != "accountName" {
		t.Fatalf("Expected %q but got %q for Segment 'AccountName'", id.AccountName, "accountName")
	}

	if id.ProjectName != "projectName" {
		t.Fatalf("Expected %q but got %q for Segment 'ProjectName'", id.ProjectName, "projectName")
	}

	if id.ConnectionName != "connectionName" {
		t.Fatalf("Expected %q but got %q for Segment 'ConnectionName'", id.ConnectionName, "connectionName")
	}
}

func TestFormatProjectConnectionID(t *testing.T) {
	actual := NewProjectConnectionID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountName", "projectName", "connectionName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.CognitiveServices/accounts/accountName/projects/projectName/connections/connectionName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

