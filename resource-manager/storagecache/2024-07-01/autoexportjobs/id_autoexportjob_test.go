func TestNewAutoExportJobID(t *testing.T) {
	id := NewAutoExportJobID("12345678-1234-9876-4563-123456789012", "example-resource-group", "amlFilesystemName", "autoExportJobName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.AmlFilesystemName != "amlFilesystemName" {
		t.Fatalf("Expected %q but got %q for Segment 'AmlFilesystemName'", id.AmlFilesystemName, "amlFilesystemName")
	}

	if id.AutoExportJobName != "autoExportJobName" {
		t.Fatalf("Expected %q but got %q for Segment 'AutoExportJobName'", id.AutoExportJobName, "autoExportJobName")
	}
}

