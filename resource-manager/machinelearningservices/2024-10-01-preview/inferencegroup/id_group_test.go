var _ resourceids.ResourceId = &GroupId{}

func TestNewGroupID(t *testing.T) {
	id := NewGroupID("12345678-1234-9876-4563-123456789012", "example-resource-group", "workspaceName", "inferencePoolName", "groupName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.WorkspaceName != "workspaceName" {
		t.Fatalf("Expected %q but got %q for Segment 'WorkspaceName'", id.WorkspaceName, "workspaceName")
	}

	if id.InferencePoolName != "inferencePoolName" {
		t.Fatalf("Expected %q but got %q for Segment 'InferencePoolName'", id.InferencePoolName, "inferencePoolName")
	}

	if id.GroupName != "groupName" {
		t.Fatalf("Expected %q but got %q for Segment 'GroupName'", id.GroupName, "groupName")
	}
}

func TestFormatGroupID(t *testing.T) {
	actual := NewGroupID("12345678-1234-9876-4563-123456789012", "example-resource-group", "workspaceName", "inferencePoolName", "groupName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.MachineLearningServices/workspaces/workspaceName/inferencePools/inferencePoolName/groups/groupName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

