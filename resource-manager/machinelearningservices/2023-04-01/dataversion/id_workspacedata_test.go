func TestNewWorkspaceDataID(t *testing.T) {
	id := NewWorkspaceDataID("12345678-1234-9876-4563-123456789012", "example-resource-group", "workspaceValue", "dataValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.WorkspaceName != "workspaceValue" {
		t.Fatalf("Expected %q but got %q for Segment 'WorkspaceName'", id.WorkspaceName, "workspaceValue")
	}

	if id.DataName != "dataValue" {
		t.Fatalf("Expected %q but got %q for Segment 'DataName'", id.DataName, "dataValue")
	}
}

func TestFormatWorkspaceDataID(t *testing.T) {
	actual := NewWorkspaceDataID("12345678-1234-9876-4563-123456789012", "example-resource-group", "workspaceValue", "dataValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.MachineLearningServices/workspaces/workspaceValue/data/dataValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

