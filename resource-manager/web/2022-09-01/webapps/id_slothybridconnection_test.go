func TestNewSlotHybridConnectionID(t *testing.T) {
	id := NewSlotHybridConnectionID("12345678-1234-9876-4563-123456789012", "example-resource-group", "siteName", "slotName", "hybridConnectionName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.SiteName != "siteName" {
		t.Fatalf("Expected %q but got %q for Segment 'SiteName'", id.SiteName, "siteName")
	}

	if id.SlotName != "slotName" {
		t.Fatalf("Expected %q but got %q for Segment 'SlotName'", id.SlotName, "slotName")
	}

	if id.HybridConnectionName != "hybridConnectionName" {
		t.Fatalf("Expected %q but got %q for Segment 'HybridConnectionName'", id.HybridConnectionName, "hybridConnectionName")
	}
}

