var _ resourceids.ResourceId = &DeploymentScriptId{}

func TestNewDeploymentScriptID(t *testing.T) {
	id := NewDeploymentScriptID("12345678-1234-9876-4563-123456789012", "example-resource-group", "deploymentScriptName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.DeploymentScriptName != "deploymentScriptName" {
		t.Fatalf("Expected %q but got %q for Segment 'DeploymentScriptName'", id.DeploymentScriptName, "deploymentScriptName")
	}
}

func TestFormatDeploymentScriptID(t *testing.T) {
	actual := NewDeploymentScriptID("12345678-1234-9876-4563-123456789012", "example-resource-group", "deploymentScriptName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Resources/deploymentScripts/deploymentScriptName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

