var _ resourceids.ResourceId = StatusId{}

func TestNewStatusID(t *testing.T) {
	id := NewStatusID("12345678-1234-9876-4563-123456789012", "example-resource-group", "experimentValue", "statusIdValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ExperimentName != "experimentValue" {
		t.Fatalf("Expected %q but got %q for Segment 'ExperimentName'", id.ExperimentName, "experimentValue")
	}

	if id.StatusId != "statusIdValue" {
		t.Fatalf("Expected %q but got %q for Segment 'StatusId'", id.StatusId, "statusIdValue")
	}
}

func TestFormatStatusID(t *testing.T) {
	actual := NewStatusID("12345678-1234-9876-4563-123456789012", "example-resource-group", "experimentValue", "statusIdValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Chaos/experiments/experimentValue/statuses/statusIdValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

