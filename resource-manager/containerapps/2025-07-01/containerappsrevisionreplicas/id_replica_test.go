func TestNewReplicaID(t *testing.T) {
	id := NewReplicaID("12345678-1234-9876-4563-123456789012", "example-resource-group", "containerAppName", "revisionName", "replicaName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ContainerAppName != "containerAppName" {
		t.Fatalf("Expected %q but got %q for Segment 'ContainerAppName'", id.ContainerAppName, "containerAppName")
	}

	if id.RevisionName != "revisionName" {
		t.Fatalf("Expected %q but got %q for Segment 'RevisionName'", id.RevisionName, "revisionName")
	}

	if id.ReplicaName != "replicaName" {
		t.Fatalf("Expected %q but got %q for Segment 'ReplicaName'", id.ReplicaName, "replicaName")
	}
}

func TestFormatReplicaID(t *testing.T) {
	actual := NewReplicaID("12345678-1234-9876-4563-123456789012", "example-resource-group", "containerAppName", "revisionName", "replicaName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.App/containerApps/containerAppName/revisions/revisionName/replicas/replicaName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

