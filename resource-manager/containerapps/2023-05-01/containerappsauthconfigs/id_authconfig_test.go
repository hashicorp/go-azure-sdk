func TestNewAuthConfigID(t *testing.T) {
	id := NewAuthConfigID("12345678-1234-9876-4563-123456789012", "example-resource-group", "containerAppValue", "authConfigValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ContainerAppName != "containerAppValue" {
		t.Fatalf("Expected %q but got %q for Segment 'ContainerAppName'", id.ContainerAppName, "containerAppValue")
	}

	if id.AuthConfigName != "authConfigValue" {
		t.Fatalf("Expected %q but got %q for Segment 'AuthConfigName'", id.AuthConfigName, "authConfigValue")
	}
}

