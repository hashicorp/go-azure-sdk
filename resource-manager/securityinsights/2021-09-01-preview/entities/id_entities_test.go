var _ resourceids.ResourceId = EntitiesId{}

func TestNewEntitiesID(t *testing.T) {
	id := NewEntitiesID("12345678-1234-9876-4563-123456789012", "example-resource-group", "workspaceValue", "entityIdValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.WorkspaceName != "workspaceValue" {
		t.Fatalf("Expected %q but got %q for Segment 'WorkspaceName'", id.WorkspaceName, "workspaceValue")
	}

	if id.EntityId != "entityIdValue" {
		t.Fatalf("Expected %q but got %q for Segment 'EntityId'", id.EntityId, "entityIdValue")
	}
}

func TestFormatEntitiesID(t *testing.T) {
	actual := NewEntitiesID("12345678-1234-9876-4563-123456789012", "example-resource-group", "workspaceValue", "entityIdValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.OperationalInsights/workspaces/workspaceValue/providers/Microsoft.SecurityInsights/entities/entityIdValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

