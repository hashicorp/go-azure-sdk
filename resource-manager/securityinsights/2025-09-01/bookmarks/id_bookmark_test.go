func TestNewBookmarkID(t *testing.T) {
	id := NewBookmarkID("12345678-1234-9876-4563-123456789012", "example-resource-group", "workspaceName", "bookmarkId")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.WorkspaceName != "workspaceName" {
		t.Fatalf("Expected %q but got %q for Segment 'WorkspaceName'", id.WorkspaceName, "workspaceName")
	}

	if id.BookmarkId != "bookmarkId" {
		t.Fatalf("Expected %q but got %q for Segment 'BookmarkId'", id.BookmarkId, "bookmarkId")
	}
}

func TestFormatBookmarkID(t *testing.T) {
	actual := NewBookmarkID("12345678-1234-9876-4563-123456789012", "example-resource-group", "workspaceName", "bookmarkId").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.OperationalInsights/workspaces/workspaceName/providers/Microsoft.SecurityInsights/bookmarks/bookmarkId"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

