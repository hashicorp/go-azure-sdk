func TestNewRelationID(t *testing.T) {
	id := NewRelationID("12345678-1234-9876-4563-123456789012", "example-resource-group", "workspaceName", "huntId", "huntRelationId")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.WorkspaceName != "workspaceName" {
		t.Fatalf("Expected %q but got %q for Segment 'WorkspaceName'", id.WorkspaceName, "workspaceName")
	}

	if id.HuntId != "huntId" {
		t.Fatalf("Expected %q but got %q for Segment 'HuntId'", id.HuntId, "huntId")
	}

	if id.HuntRelationId != "huntRelationId" {
		t.Fatalf("Expected %q but got %q for Segment 'HuntRelationId'", id.HuntRelationId, "huntRelationId")
	}
}

func TestFormatRelationID(t *testing.T) {
	actual := NewRelationID("12345678-1234-9876-4563-123456789012", "example-resource-group", "workspaceName", "huntId", "huntRelationId").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.OperationalInsights/workspaces/workspaceName/providers/Microsoft.SecurityInsights/hunts/huntId/relations/huntRelationId"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

