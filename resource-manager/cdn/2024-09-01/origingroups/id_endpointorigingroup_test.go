func TestNewEndpointOriginGroupID(t *testing.T) {
	id := NewEndpointOriginGroupID("12345678-1234-9876-4563-123456789012", "example-resource-group", "profileName", "endpointName", "originGroupName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ProfileName != "profileName" {
		t.Fatalf("Expected %q but got %q for Segment 'ProfileName'", id.ProfileName, "profileName")
	}

	if id.EndpointName != "endpointName" {
		t.Fatalf("Expected %q but got %q for Segment 'EndpointName'", id.EndpointName, "endpointName")
	}

	if id.OriginGroupName != "originGroupName" {
		t.Fatalf("Expected %q but got %q for Segment 'OriginGroupName'", id.OriginGroupName, "originGroupName")
	}
}

