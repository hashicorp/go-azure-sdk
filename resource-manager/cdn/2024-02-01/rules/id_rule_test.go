func TestNewRuleID(t *testing.T) {
	id := NewRuleID("12345678-1234-9876-4563-123456789012", "example-resource-group", "profileName", "ruleSetName", "ruleName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ProfileName != "profileName" {
		t.Fatalf("Expected %q but got %q for Segment 'ProfileName'", id.ProfileName, "profileName")
	}

	if id.RuleSetName != "ruleSetName" {
		t.Fatalf("Expected %q but got %q for Segment 'RuleSetName'", id.RuleSetName, "ruleSetName")
	}

	if id.RuleName != "ruleName" {
		t.Fatalf("Expected %q but got %q for Segment 'RuleName'", id.RuleName, "ruleName")
	}
}

func TestFormatRuleID(t *testing.T) {
	actual := NewRuleID("12345678-1234-9876-4563-123456789012", "example-resource-group", "profileName", "ruleSetName", "ruleName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Cdn/profiles/profileName/ruleSets/ruleSetName/rules/ruleName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

