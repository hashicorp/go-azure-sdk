func TestNewOriginGroupOriginID(t *testing.T) {
	id := NewOriginGroupOriginID("12345678-1234-9876-4563-123456789012", "example-resource-group", "profileValue", "originGroupValue", "originValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ProfileName != "profileValue" {
		t.Fatalf("Expected %q but got %q for Segment 'ProfileName'", id.ProfileName, "profileValue")
	}

	if id.OriginGroupName != "originGroupValue" {
		t.Fatalf("Expected %q but got %q for Segment 'OriginGroupName'", id.OriginGroupName, "originGroupValue")
	}

	if id.OriginName != "originValue" {
		t.Fatalf("Expected %q but got %q for Segment 'OriginName'", id.OriginName, "originValue")
	}
}

