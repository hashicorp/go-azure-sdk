func TestNewDiscoveredDeviceID(t *testing.T) {
	id := NewDiscoveredDeviceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "namespaceName", "discoveredDeviceName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.NamespaceName != "namespaceName" {
		t.Fatalf("Expected %q but got %q for Segment 'NamespaceName'", id.NamespaceName, "namespaceName")
	}

	if id.DiscoveredDeviceName != "discoveredDeviceName" {
		t.Fatalf("Expected %q but got %q for Segment 'DiscoveredDeviceName'", id.DiscoveredDeviceName, "discoveredDeviceName")
	}
}

func TestFormatDiscoveredDeviceID(t *testing.T) {
	actual := NewDiscoveredDeviceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "namespaceName", "discoveredDeviceName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.DeviceRegistry/namespaces/namespaceName/discoveredDevices/discoveredDeviceName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

