func TestNewDiscoveredAssetID(t *testing.T) {
	id := NewDiscoveredAssetID("12345678-1234-9876-4563-123456789012", "example-resource-group", "namespaceName", "discoveredAssetName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.NamespaceName != "namespaceName" {
		t.Fatalf("Expected %q but got %q for Segment 'NamespaceName'", id.NamespaceName, "namespaceName")
	}

	if id.DiscoveredAssetName != "discoveredAssetName" {
		t.Fatalf("Expected %q but got %q for Segment 'DiscoveredAssetName'", id.DiscoveredAssetName, "discoveredAssetName")
	}
}

func TestFormatDiscoveredAssetID(t *testing.T) {
	actual := NewDiscoveredAssetID("12345678-1234-9876-4563-123456789012", "example-resource-group", "namespaceName", "discoveredAssetName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.DeviceRegistry/namespaces/namespaceName/discoveredAssets/discoveredAssetName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

