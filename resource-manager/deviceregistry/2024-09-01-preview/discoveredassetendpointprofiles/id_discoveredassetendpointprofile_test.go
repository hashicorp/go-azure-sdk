func TestNewDiscoveredAssetEndpointProfileID(t *testing.T) {
	id := NewDiscoveredAssetEndpointProfileID("12345678-1234-9876-4563-123456789012", "example-resource-group", "discoveredAssetEndpointProfileName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.DiscoveredAssetEndpointProfileName != "discoveredAssetEndpointProfileName" {
		t.Fatalf("Expected %q but got %q for Segment 'DiscoveredAssetEndpointProfileName'", id.DiscoveredAssetEndpointProfileName, "discoveredAssetEndpointProfileName")
	}
}

