var _ resourceids.ResourceId = LibraryId{}

func TestNewLibraryID(t *testing.T) {
	id := NewLibraryID("12345678-1234-9876-4563-123456789012", "example-resource-group", "workspaceValue", "libraryValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.WorkspaceName != "workspaceValue" {
		t.Fatalf("Expected %q but got %q for Segment 'WorkspaceName'", id.WorkspaceName, "workspaceValue")
	}

	if id.LibraryName != "libraryValue" {
		t.Fatalf("Expected %q but got %q for Segment 'LibraryName'", id.LibraryName, "libraryValue")
	}
}

func TestFormatLibraryID(t *testing.T) {
	actual := NewLibraryID("12345678-1234-9876-4563-123456789012", "example-resource-group", "workspaceValue", "libraryValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Synapse/workspaces/workspaceValue/libraries/libraryValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

