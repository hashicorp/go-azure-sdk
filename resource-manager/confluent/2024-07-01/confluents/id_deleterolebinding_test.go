func TestNewDeleteRoleBindingID(t *testing.T) {
	id := NewDeleteRoleBindingID("12345678-1234-9876-4563-123456789012", "example-resource-group", "organizationName", "roleBindingId")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.OrganizationName != "organizationName" {
		t.Fatalf("Expected %q but got %q for Segment 'OrganizationName'", id.OrganizationName, "organizationName")
	}

	if id.RoleBindingId != "roleBindingId" {
		t.Fatalf("Expected %q but got %q for Segment 'RoleBindingId'", id.RoleBindingId, "roleBindingId")
	}
}

