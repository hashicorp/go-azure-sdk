var _ resourceids.ResourceId = &ValidationId{}

func TestNewValidationID(t *testing.T) {
	id := NewValidationID("12345678-1234-9876-4563-123456789012", "example-resource-group", "validationName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ValidationName != "validationName" {
		t.Fatalf("Expected %q but got %q for Segment 'ValidationName'", id.ValidationName, "validationName")
	}
}

func TestFormatValidationID(t *testing.T) {
	actual := NewValidationID("12345678-1234-9876-4563-123456789012", "example-resource-group", "validationName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Confluent/validations/validationName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

