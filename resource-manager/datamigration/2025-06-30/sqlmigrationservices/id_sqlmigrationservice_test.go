func TestNewSqlMigrationServiceID(t *testing.T) {
	id := NewSqlMigrationServiceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "sqlMigrationServiceName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.SqlMigrationServiceName != "sqlMigrationServiceName" {
		t.Fatalf("Expected %q but got %q for Segment 'SqlMigrationServiceName'", id.SqlMigrationServiceName, "sqlMigrationServiceName")
	}
}

